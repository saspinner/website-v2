import { defineCollection } from 'astro:content';
import { glob} from 'astro/loaders';
import { z } from 'astro/zod';

const blog = defineCollection({
  loader: glob({ pattern: "*.md", base: "src/posts" }),
  schema: z.object({
    title: z.string(),
    date: z.coerce.date(),
    live: z.boolean(),
  }),
});

export const collections = { blog };
